    private void jButton29ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton29ActionPerformed

        Ubicacion ubicacion = new Ubicacion(addProductPostalCode.getText(), addProductCity.getText());

        int id = controller.getProductos().size() + 1;

        boolean urgente = jCheckBox1.isSelected();

        Producto producto = new Producto(id, addProductTitle.getText(),
                addProductDescription.getText(),
                Categoria.valueOf((String) addProductCategory.getSelectedItem()),
                EstadoProducto.valueOf((String) addProductStatus.getSelectedItem()), Double.valueOf(addProductPrice.getText()),
                LocalDate.now(), ubicacion, cliente.getDni());

        producto.setUrgente(urgente);
        controller.addProducto(producto);

        if (esProfesional) {
            changePanel(profesionalPanel);
        } else {
            changePanel(clientPanel);
        }
    }//GEN-LAST:event_jButton29ActionPerformed

