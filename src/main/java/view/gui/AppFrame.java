    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // TODO add your handling code here:

        if (register_dni.getText().length() != 8) {
            JOptionPane.showMessageDialog(this, "El dni tiene que ser de 8 caracteres", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (register_tarjeta.getText().length() != 16) {
            JOptionPane.showMessageDialog(this, "La tarjeta tiene que ser de 16 caracteres", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Ubicacion ubicacion = new Ubicacion(register_postal.getText(), register_city.getText());
        Cliente nuevoCliente;
        nuevoCliente = new Cliente(register_dni.getText(), register_name.getText(),
                register_email.getText(), new String(register_password.getPassword()),
                ubicacion, register_tarjeta.getText());

        boolean registrado = controller.addCliente(nuevoCliente);
        if (registrado) {
            this.cliente = nuevoCliente;
            JOptionPane.showMessageDialog(this, "Inicia sesión", "Bienvenido a Javapop", JOptionPane.INFORMATION_MESSAGE);
            changePanel(loginPanel);
        } else {
            register_dni.setText(null);
            register_name.setText(null);
            register_postal.setText(null);
            register_city.setText(null);
            register_tarjeta.setText(null);
            register_email.setText(null);
            register_password.setText(null);
            JOptionPane.showMessageDialog(this, "Usuario ya registrado", "Error", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_jButton11ActionPerformed

