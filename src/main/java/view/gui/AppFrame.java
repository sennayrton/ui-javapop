    private void jButton20ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton20ActionPerformed
        // TODO add your handling code here:

        List<Producto> productos = controller.getProductos();

        Object[][] arrayProductos = new Object[productos.size()][6];

        for (int i = 0; i < productos.size(); i++) {
            Producto producto = productos.get(i);
            arrayProductos[i] = new Object[]{producto.getId(), producto.getTitulo(), producto.getCategoria(), producto.getEstado(), producto.getPrecio(), producto.getFecha()};
        }
        jTable2.setModel(new javax.swing.table.DefaultTableModel(
                arrayProductos,
                new String[]{
                    "Id", "Titulo", "Categoria", "Estado", "Precio", "Fecha"
                }
        ) {
            boolean[] canEdit = new boolean[]{
                false, false, false, false
            };

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit[columnIndex];
            }
        });

        changePanel(buyProductPanel);

    }//GEN-LAST:event_jButton20ActionPerformed

