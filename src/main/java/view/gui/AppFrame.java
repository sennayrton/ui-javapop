    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        // TODO add your handling code here:

        Cliente response = controller.login(login_email.getText(), new String(login_password.getPassword()));
        if (controller.isAdmin(login_email.getText(), new String(login_password.getPassword()))) {
            changePanel(adminPanel);
        } else if (response == null) {
            JOptionPane.showMessageDialog(this, "Credenciales Incorrectas", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (controller.isProfesional(response)) {
            changePanel(profesionalPanel);
            this.cliente = response;
            this.esProfesional = true;
            nameProfesional.setText("Bienvenido a Javapop, " + cliente.getNombre());
        } else {
            changePanel(clientPanel);
            this.cliente = response;
            this.esProfesional = false;
            nameCliente.setText("Bienvenido a Javapop, " + cliente.getNombre());
        }
        login_email.setText(null);
        login_password.setText(null);

    }//GEN-LAST:event_loginButtonActionPerformed

