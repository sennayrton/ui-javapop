    private void jButton24ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton24ActionPerformed
        // TODO add your handling code here:

        Categoria categoria = Categoria.valueOf((String) categoriaProductos.getSelectedItem());

        List<Producto> productos;
        productos = controller.find(categoria, buscarPalabrasClave.getText(), cliente);

        String[] colums = new String[]{"Id", "Titulo", "Estado", "Precio"};

        Object[][] datos = new Object[productos.size()][colums.length];

        for (int i = 0; i < datos.length; i++) {
            Producto producto = productos.get(i);
            datos[i] = new Object[]{producto.getId(), producto.getTitulo(), producto.getEstado().name(), producto.getPrecio()};
        }

        productosTable.setModel(new javax.swing.table.DefaultTableModel(datos, colums));

        changePanel(resultadoBuscarProductosPanel);
    }//GEN-LAST:event_jButton24ActionPerformed

