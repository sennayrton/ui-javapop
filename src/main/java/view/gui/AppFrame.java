    private void changePanel(JPanel panel) {

        jPanel1.removeAll();
        panel.setVisible(true);
        jPanel1.add(panel);

        if (panel == initPanel || panel == registerPanel || panel == loginPanel) {
            panel_bottom.setVisible(false);
        } else {
            panel_bottom.setVisible(true);
        }

        this.validate();
        this.update(this.getGraphics());

    }

